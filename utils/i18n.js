var currentLang = 'zh'

function getLang() {
  try {
    var deviceInfo = wx.getDeviceInfo()
    var lang = deviceInfo.language || 'zh'
    if (lang.indexOf('zh') >= 0) {
      return 'zh'
    } else {
      return 'en'
    }
  } catch (e) {
    return 'zh'
  }
}

function t(key) {
  var lang = i18n[currentLang] || i18n['zh']
  return lang[key] || key
}

function initLang() {
  currentLang = getLang()
  wx.setStorageSync('app_lang', currentLang)
}

function setLang(lang) {
  currentLang = lang
  wx.setStorageSync('app_lang', lang)
}

function getLanguage() {
  return currentLang
}

module.exports = {
  t: t,
  initLang: initLang,
  setLang: setLang,
  getLanguage: getLanguage,
  i18n: i18n
}
